  const supportedLanguages = Object.keys(translations);

  function getDefaultLanguage() {
    const input = document.getElementById("defaultlanguage");
    const value = input?.value?.trim().toLowerCase();
    return value || null;
  }

  function getStoredLanguage() {
    return localStorage.getItem("site_language");
  }

  function getPreferredLanguage() {
    const stored = getStoredLanguage();
    if (stored && supportedLanguages.includes(stored)) {
      return stored;
    }
    const fallback = getDefaultLanguage();
    return supportedLanguages.includes(fallback) ? fallback : "tr";
  }

  function applyTranslations() {
    const lang = getPreferredLanguage();
    const dict = translations[lang] || translations.tr;
    if (typeof window.SITE_TITLE_BRAND === "undefined") {
      const meta = document.querySelector('meta[name="site-title-brand"]');
      window.SITE_TITLE_BRAND = meta ? (meta.getAttribute("content") || "YepPos") : "YepPos";
    }

    document.querySelectorAll("[data-i18n]").forEach((el) => {
      const key = el.getAttribute("data-i18n");
      if (key && dict[key] !== undefined) {
        el.textContent = dict[key];
      }
    });
    document.querySelectorAll("title[data-i18n]").forEach((el) => {
      const brand = window.SITE_TITLE_BRAND || "YepPos";
      if (el.textContent && el.textContent.indexOf("YepPos") === 0) {
        el.textContent = brand + el.textContent.slice(6);
      }
    });

    document.querySelectorAll("[data-i18n-html]").forEach((el) => {
      const key = el.getAttribute("data-i18n-html");
      if (key && dict[key] !== undefined) {
        el.innerHTML = dict[key];
      }
    });

    document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
      const key = el.getAttribute("data-i18n-placeholder");
      if (key && dict[key] !== undefined) {
        el.setAttribute("placeholder", dict[key]);
      }
    });

    document.querySelectorAll("[data-i18n-title]").forEach((el) => {
      const key = el.getAttribute("data-i18n-title");
      if (key && dict[key] !== undefined) {
        el.setAttribute("title", dict[key]);
      }
    });

    document.querySelectorAll("[data-i18n-aria-label]").forEach((el) => {
      const key = el.getAttribute("data-i18n-aria-label");
      if (key && dict[key] !== undefined) {
        el.setAttribute("aria-label", dict[key]);
      }
    });

    document.title = lang === "en"
      ? "Tahmisçi | Coffee & Roastery"
      : "Tahmisçi | Coffee & Roastery";
    const description = document.querySelector('meta[name="description"]');
    if (description) {
      description.setAttribute("content", lang === "en"
        ? "Tahmisçi Coffee & Roastery brings carefully prepared coffee, cold drinks and desserts together."
        : "Tahmisçi Coffee & Roastery; özenle hazırlanan kahveler, soğuk içecekler ve tatlıları buluşturur.");
    }
  }

  function setLanguage(lang) {
    const normalized = supportedLanguages.includes(lang) ? lang : "tr";
    localStorage.setItem("site_language", normalized);
    document.documentElement.lang = normalized;
    window.AppLanguage = normalized;
    applyTranslations();
    window.dispatchEvent(
      new CustomEvent("languageChanged", { detail: { language: normalized } }),
    );
  }

  function t(key) {
    const lang = getPreferredLanguage();
    const dict = translations[lang] || translations.tr;
    return dict[key] !== undefined ? dict[key] : key;
  }

  window.I18N = {
    setLanguage,
    applyTranslations,
    getPreferredLanguage,
    getTranslations: () => translations,
    t,
  };

  document.addEventListener("DOMContentLoaded", () => {
    const initialLang = getPreferredLanguage();
    document.documentElement.lang = initialLang;
    window.AppLanguage = initialLang;
    applyTranslations();
  });
